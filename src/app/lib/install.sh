#!/bin/bash

# Qwabi Family Website Setup Script

# Exit immediately if a command exits with a non-zero status
set -e

echo "Starting Qwabi Family Website setup..."

# Install dependencies
echo "Installing dependencies..."
npm install tailwindcss postcss autoprefixer @tailwindcss/forms
npm install @radix-ui/react-select @radix-ui/react-label @radix-ui/react-alert-dialog
npm install axios

# Install shadcn/ui CLI
echo "Installing shadcn/ui CLI..."
npm install -D @shadcn/ui

# Initialize Tailwind CSS
echo "Initializing Tailwind CSS..."
npx tailwindcss init -p

# Update tailwind.config.js
echo "Updating tailwind.config.js..."
cat > tailwind.config.js << EOL
